use fast_qr::Version;
pub fn generate_version(len: usize) -> Version {
    match len {
        0..=7 => Version::V01,
        8..=14 => Version::V02,
        15..=24 => Version::V03,
        25..=34 => Version::V04,
        35..=44 => Version::V05,
        45..=58 => Version::V06,
        59..=64 => Version::V07,
        65..=84 => Version::V08,
        85..=98 => Version::V09,
        99..=119 => Version::V10,
        120..=137 => Version::V11,
        138..=155 => Version::V12,
        156..=177 => Version::V13,
        178..=194 => Version::V14,
        195..=220 => Version::V15,
        221..=250 => Version::V16,
        251..=280 => Version::V17,
        281..=310 => Version::V18,
        311..=338 => Version::V19,
        339..=382 => Version::V20,
        383..=403 => Version::V21,
        404..=439 => Version::V22,
        440..=461 => Version::V23,
        462..=511 => Version::V24,
        512..=535 => Version::V25,
        536..=593 => Version::V26,
        594..=625 => Version::V27,
        626..=658 => Version::V28,
        659..=698 => Version::V29,
        699..=742 => Version::V30,
        743..=790 => Version::V31,
        791..=842 => Version::V32,
        843..=898 => Version::V33,
        899..=958 => Version::V34,
        959..=983 => Version::V35,
        984..=1051 => Version::V36,
        1052..=1093 => Version::V37,
        1094..=1139 => Version::V38,
        1140..=1219 => Version::V39,
        1220..=1273 => Version::V40,
        _ => generate_version(1273),
    }
}
